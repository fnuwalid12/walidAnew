package basics;

public class StringBasics {

	public static void main(String[] args) {
		
		// escape sequence system will not understand "" it will think this is a new code
		// we will put \ before using "" to tell the system to escape what ever is after \
		
	// this "OK"!	
		
	System.out.println("this is \"OK\"!");	
	System.out.println("\this is one tab"); // to add one tab or indent \t
	System.out.println("this is \ngoing to the next line"); // to go to the next line \n
	
	System.out.println("this is \n\n\tgoing \n\t\tto the next line"); // to go to the next line \n

	
	
	
	
	
	}

}
